export const getAssetsByCategory = (category) => {
    return CHARACTER_ASSETS[category] || [];
};


export const getAssetById = (category, id) => {
    const assets = CHARACTER_ASSETS[category];
    if (!assets) return undefined;
    if (Array.isArray(assets[0])) {
        for (let i = 0; i < assets.length; i++) {
            const layer = assets[i];
            const found = layer.find(asset => asset.id === id);
            if (found) return found;
        }
        return undefined;
    }
    return assets.find(asset => asset.id === id);
};


export const getRandomAsset = (category) => {
    const assets = CHARACTER_ASSETS[category];
    if (!assets || assets.length === 0) return null;

    if (Array.isArray(assets[0])) {
        const randomLayer = assets[Math.floor(Math.random() * assets.length)];

        return randomLayer[Math.floor(Math.random() * randomLayer.length)];
    }

    return assets[Math.floor(Math.random() * assets.length)];
};
export const getAssetFromLayer = (category, layerIndex, id) => {
    const assets = CHARACTER_ASSETS[category];
    if (!assets || !Array.isArray(assets[0]) || !assets[layerIndex]) return undefined;

    return assets[layerIndex].find(asset => asset.id === id);
};

export const getRandomAssetFromLayer = (category, layerIndex) => {
    const assets = CHARACTER_ASSETS[category];
    if (!assets || !Array.isArray(assets[0]) || !assets[layerIndex]) return null;

    const layer = assets[layerIndex];
    return layer[Math.floor(Math.random() * layer.length)];
};
